package com.mama.jedis.service.facade.request;

/**
 * @author chenmeiyang
 * Redis数据库枚举类
 */
public enum RedisDBEnum {
	
	QIANBAO("钱包" , "qianbao"),
	HOUSE("送房" ,   "house");
	
	private String name;
	private String code;
	
	private RedisDBEnum(String name , String code){
		this.name = name;
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
	

}
